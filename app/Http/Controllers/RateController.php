    {
        $action = new Rate();
        $data = $request->all();
        $result = $action->addRate($data);
        return $result;
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id, Request $request)
    {
        $action = new Rate();
        $order = $request->order;
        $dish = $request->dish;
        if (isset($order)) {
            $result = $action->getDishRateByOrderid($id);
        }

        if (isset($dish)) {
            $result = $action->getDishRateByDishid($id);
        }

        return $result;
    }


